using System;

namespace Solution
{
    public class SolutionClass
    {
        public static string EvenOrOdd(int number)
        {
            // if the number modulus 2, is equal to 0, 
            if (number % 2 = 0)
            {
                //the number is even
                return "Even";
            }
            {
                return "Odd";
                // else the number is odd
            }
        }
    }
}